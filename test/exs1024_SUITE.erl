all() ->
    [testloop_test].

-spec testloop(pos_integer()) -> list().

testloop(N) ->
    R = exs1024:seed0(),
    testloop(N, R, []).

testloop(0, _, L) ->
    lists:reverse(L);
testloop(N, R, L) ->
    {V, R1} = exs1024:next(R),
    testloop(N - 1, R1, [V|L]).

testloop_test(_Config) ->
    Refval = test_refval(),
    Testval = testloop(length(Refval)),
    case Refval =:= Testval of
        true -> ok;
        Else -> exit(Else)
    end.

